import chop

assert chop(0.00000000000000001) == 0.0
assert chop(-0.00000000000000001) == 0.0
assert chop(0.01) == 0.01
assert chop(-0.01) == -0.01

assert chop(@[0.00000000000000001, 0.01, 0.1, 1.0, 10.0]) == @[0.0, 0.01, 0.1, 1.0, 10.0]

assert chop(@[@[@[-1.0, 0.0, 1.0]]]) == @[@[@[-1.0, 0.0, 1.0]]]
assert chop(0.01, 0.1) == 0.0
